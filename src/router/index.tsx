//----------------------------------------------------------------------------------------------------------------------
// Imports
//----------------------------------------------------------------------------------------------------------------------

import { lazy } from 'react'
import {createMemoryRouter} from "react-router-dom"
import App from "../App"

//----------------------------------------------------------------------------------------------------------------------
// Pages
//----------------------------------------------------------------------------------------------------------------------

const MainLazyPage = lazy(() => import('../pages/Main'))

//----------------------------------------------------------------------------------------------------------------------
// Router
//----------------------------------------------------------------------------------------------------------------------

const router = createMemoryRouter([
	{
		path: "/",
		element: <MainLazyPage/>,
	},{
		path: "/main",
		element: <MainLazyPage/>,
		id: 'main'
	},{
		path: "/app",
		element: <App/>,
		id: 'app'
	},
]);

//----------------------------------------------------------------------------------------------------------------------
// Exports
//----------------------------------------------------------------------------------------------------------------------

export default router
