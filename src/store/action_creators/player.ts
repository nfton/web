import { Dispatch } from 'react'
import { EPlayerActionTypes, INFT, TPlayerActions } from '../../types'

//----------------------------------------------------------------------------------------------------------------------
// Set New Fit Action
//----------------------------------------------------------------------------------------------------------------------

export const setNewFit = (payload: INFT) => {
	return async (dispatch: Dispatch<TPlayerActions>) => {
		dispatch({
			type: EPlayerActionTypes.SET_NEW_FIT,
			payload
		})
	}
}
